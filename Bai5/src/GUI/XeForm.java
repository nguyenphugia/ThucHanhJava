    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        if (txtSoXe.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập thông tin");
        }
        else{
            if (BLL.getID(txtSoXe.getText())){
                    JOptionPane.showMessageDialog(null, "Số xe bị trùng.");
            }
            else {
                String soxe = txtSoXe.getText();
                String tinhtrang = txtTinhTrang.getText();
                String maloai = cbMaLoai.getSelectedItem().toString();
                Xe x = new Xe (soxe, tinhtrang, maloai);
                BLL.Sua(x);
                BLL.loadDsx(jTable1);
                JOptionPane.showMessageDialog(null, "Thêm thành công.");
            }
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        int iDongDaChon = jTable1.getSelectedRow();
        if (iDongDaChon == -1){
            JOptionPane.showMessageDialog(null, "Mời chọn xe");
        }
        else{
            if(JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa: \"" + txtSoXe.getText().toUpperCase() + "\" không?", "Thông Báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                BLL.Xoa(txtSoXe.getText());
                BLL.loadDsx(jTable1);
                    }
        }
    }//GEN-LAST:event_btXoaActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        if (txtSoXe.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Chọn nhật ký cần sửa");
        }
       else {
            String soxe = txtSoXe.getText();
            String tinhtrang = txtTinhTrang.getText();
            String maloai = cbMaLoai.getSelectedItem().toString();
            Xe x = new Xe (soxe, tinhtrang, maloai);
            BLL.Sua(x);
            BLL.loadDsx(jTable1);
            ClearAll();
             JOptionPane.showMessageDialog(null, "Sửa thành công.");
       }
    }//GEN-LAST:event_btSuaActionPerformed

