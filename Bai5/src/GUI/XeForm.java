    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int iDongDaChon = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        txtSoXe.setText(model.getValueAt(iDongDaChon, 0).toString());
        txtTinhTrang.setText(model.getValueAt(iDongDaChon, 1).toString());
        cbMaLoai.setSelectedItem(model.getValueAt(iDongDaChon, 2).toString());
    }//GEN-LAST:event_jTable1MouseClicked

